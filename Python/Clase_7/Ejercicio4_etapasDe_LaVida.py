edad = int(input("Ingrese su edad: "))
if (0< edad <= 10):
    print('La infancia es increibre y bella')
elif (11< edad <= 19):
    print('Tienes muchos cambios, mucho que estudiar')
elif (20< edad <= 29):
    print('Amor y comienza a trabajar')
elif (30< edad <= 39):
    print('La madurez llega, cosecha lo sembrado')
elif (40< edad <= 49):
    print('La plenitud de la vida, experiencia y sabiduría')
elif (50< edad <= 59):
    print('Reflexión profunda, disfruta lo logrado')
elif (60< edad <= 69):
    print('La serenidad llega, tiempo para ti y los tuyos')
elif (70> edad <= 79):
    print('La vejez trae paz, recuerdos y gratitud')
elif (80> edad <= 89):
    print('Una vida plena, eres fuerte de sabiduría')
elif (90> edad <= 99):
    print('Leyenda viva, cada día es un regalo invaluable')
else:
    print('Edad fuera del rango válido')